package com.example.wang.backendlessmap;

public class MyLocation {

    private double X,Y;

    public MyLocation() {
        X = 0;
        Y = 0;
    }

    public double getX() {
        return X;
    }

    public void setX(double x) {
        X = x;
    }

    public double getY() {
        return Y;
    }

    public void setY(double y) {
        Y = y;
    }
}
